num1=parseInt (prompt ('Ingrese el primer número'));
num2=parseInt (prompt ('Ingrese el segundo número'));

if (num1 > num2) {
    console.log (`El primer número es mayor que el segundo, la suma de ambos es igual a ${num1+num2}`);
} else if (num1 < num2){
    console.log (`El primer número es menor que el segundo, la resta de ${num2} menos ${num1} es igual a ${num2-num1}`);
} else {
    console.log (`Ambos números son iguales, el producto entre ambos es igual a ${num1*num2}`);
}





